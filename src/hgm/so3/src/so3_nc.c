  oxprintfe("Usage: so3_nc a b c returns nc(a,b,c) and its gradients\n");
  oxprintfe("   where nc is the normalization constant\n" );
  oxprintfe("   of the Fisher distribution on SO(3) for the diagonal matrix diag(a,b,c).\n");
